    void calculo(int d)
    {
        double pxd = Double.valueOf(getPrecioxDia.getText()), totalR;
        
        
        if(getPrecioOferta.getText().equals(""))
        {
            totalR = (double) d * pxd;
        }else{
            double po = Double.valueOf(getPrecioOferta.getText());
            po = (((pxd*d)*po)/100);
            totalR = (((double) d * pxd)-po);
        }
        totalReserva.setText(String.valueOf(totalR));
    }
    
    
